package com.shakawat21.retrofit.workWithTwoAdapter;

public class Model_2_1 {

    private String resource;
    private int id;
    private String name;


    public Model_2_1() {
    }

    public Model_2_1(String resource, int id, String name) {
        this.resource = resource;
        this.id = id;
        this.name = name;
    }

    public String getResource() {
        return resource;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
